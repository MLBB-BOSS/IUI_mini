def format_unique_analytics_text(user_name: str, derived_data: Optional[Dict[str, Any]]) -> str:
    """–§–æ—Ä–º–∞—Ç—É—î —É–Ω—ñ–∫–∞–ª—å–Ω—É –∞–Ω–∞–ª—ñ—Ç–∏–∫—É –≥—Ä–∞–≤—Ü—è —É —á–∏—Ç–∞–±–µ–ª—å–Ω–∏–π HTML."""
    if not derived_data:
        return f"–ù–µ –≤–¥–∞–ª–æ—Å—è —Ä–æ–∑—Ä–∞—Ö—É–≤–∞—Ç–∏ —É–Ω—ñ–∫–∞–ª—å–Ω—É –∞–Ω–∞–ª—ñ—Ç–∏–∫—É –¥–ª—è {html.escape(user_name)}."

    parts = [f"<b>üìà <u>–£–Ω—ñ–∫–∞–ª—å–Ω–∞ –ê–Ω–∞–ª—ñ—Ç–∏–∫–∞ –≤—ñ–¥ IUI –¥–ª—è {html.escape(user_name)}:</u></b>"]
    
    has_data = False

    total_wins = derived_data.get('total_wins')
    total_losses = derived_data.get('total_losses', 'N/A')
    if total_wins is not None:
        parts.append(f"  üëë –ü–µ—Ä–µ–º–æ–≥/–ü–æ—Ä–∞–∑–æ–∫: <b>{total_wins} / {total_losses}</b>")
        has_data = True
    
    mvp_rate = derived_data.get('mvp_rate_percent')
    if mvp_rate is not None:
        parts.append(f"  ‚≠ê MVP –†–µ–π—Ç–∏–Ω–≥: <b>{mvp_rate:.2f}%</b> –º–∞—Ç—á—ñ–≤")
        has_data = True
        
    mvp_win_share = derived_data.get('mvp_win_share_percent')
    if mvp_win_share is not None:
        parts.append(f"  üèÜ –ß–∞—Å—Ç–∫–∞ MVP —É –ø–µ—Ä–µ–º–æ–≥–∞—Ö: <b>{mvp_win_share:.2f}%</b>")
        has_data = True
        
    savage_freq = derived_data.get('savage_frequency_per_1000_matches')
    if savage_freq is not None:
        parts.append(f"  üî• –ß–∞—Å—Ç–æ—Ç–∞ Savage: ~<b>{savage_freq:.2f}</b> –Ω–∞ 1000 –º–∞—Ç—á—ñ–≤")
        has_data = True
        
    legendary_freq = derived_data.get('legendary_frequency_per_100_matches')
    if legendary_freq is not None:
        parts.append(f"  ‚ú® –ß–∞—Å—Ç–æ—Ç–∞ Legendary: ~<b>{legendary_freq:.2f}</b> –Ω–∞ 100 –º–∞—Ç—á—ñ–≤")
        has_data = True
        
    dmg_gold_ratio = derived_data.get('damage_per_gold_ratio')
    if dmg_gold_ratio is not None:
        parts.append(f"  ‚öîÔ∏è –ï—Ñ–µ–∫—Ç–∏–≤–Ω—ñ—Å—Ç—å –∑–æ–ª–æ—Ç–∞: <b>{dmg_gold_ratio:.2f}</b> —à–∫–æ–¥–∏/—Ö–≤ –Ω–∞ 1 –∑–æ–ª–æ—Ç–æ/—Ö–≤")
        has_data = True
        
    avg_impact = derived_data.get('avg_impact_score_per_match')
    if avg_impact is not None:
        parts.append(f"  üéØ –°–µ—Ä. –í–ø–ª–∏–≤ (K+A)/–º–∞—Ç—á: ~<b>{avg_impact:.2f}</b>")
        has_data = True
    
    if not has_data:
        return f"–î–ª—è –≥—Ä–∞–≤—Ü—è {html.escape(user_name)} –Ω–µ–¥–æ—Å—Ç–∞—Ç–Ω—å–æ –¥–∞–Ω–∏—Ö –¥–ª—è —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É —É–Ω—ñ–∫–∞–ª—å–Ω–æ—ó –∞–Ω–∞–ª—ñ—Ç–∏–∫–∏."
        
    return "\n".join(parts)

