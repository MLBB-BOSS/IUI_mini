def format_profile_result(user_name: str, data: Dict[str, Any]) -> str:
    """
    –§–æ—Ä–º–∞—Ç—É—î —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏ –∞–Ω–∞–ª—ñ–∑—É –ø—Ä–æ—Ñ—ñ–ª—é.
    """
    if not data:
        return f"–ù–µ –≤–¥–∞–ª–æ—Å—è —Ä–æ–∑–ø—ñ–∑–Ω–∞—Ç–∏ –¥–∞–Ω—ñ –ø—Ä–æ—Ñ—ñ–ª—é –¥–ª—è {html.escape(user_name)}."

    parts = [f"<b>üìã –ü—Ä–æ—Ñ—ñ–ª—å –≥—Ä–∞–≤—Ü—è {html.escape(user_name)}:</b>"]
    
    fields = {
        "game_nickname": "üéÆ –ù—ñ–∫–Ω–µ–π–º",
        "mlbb_id_server": "üÜî ID (–°–µ—Ä–≤–µ—Ä)",
        "highest_rank_season": "üåü –ù–∞–π–≤–∏—â–∏–π —Ä–∞–Ω–≥",
        "matches_played": "‚öîÔ∏è –ú–∞—Ç—á—ñ–≤ –∑—ñ–≥—Ä–∞–Ω–æ",
        "likes_received": "üëç –õ–∞–π–∫—ñ–≤ –æ—Ç—Ä–∏–º–∞–Ω–æ",
        "location": "üåç –õ–æ–∫–∞—Ü—ñ—è",
        "squad_name": "üõ°Ô∏è –°–∫–≤–∞–¥"
    }
    
    has_data = False
    for key, label in fields.items():
        value = data.get(key)
        if value is not None:
            parts.append(AnalysisFormatter._format_field(label, value))
            has_data = True
    
    if not has_data:
        parts.append("<i>‚ö†Ô∏è –ù–µ –≤–¥–∞–ª–æ—Å—è —Ä–æ–∑–ø—ñ–∑–Ω–∞—Ç–∏ –¥–∞–Ω—ñ. –°–ø—Ä–æ–±—É–π—Ç–µ —á—ñ—Ç–∫—ñ—à–∏–π —Å–∫—Ä—ñ–Ω—à–æ—Ç.</i>")
    
    return "\n".join(parts)

