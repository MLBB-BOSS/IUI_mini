"""
Модуль для надання цікавих фактів на основі часу реакції гравця.
"""
import random

# Список фактів, де кожен елемент - це кортеж:
# ((min_ms, max_ms), "Текст факту")
REACTION_FACTS = [
    ((0, 150), "Це швидше, ніж моргання ока (~150 мс)! Ти справжній кіберспортсмен!"),
    ((151, 200), "Професійні гравці в шутери мають середню реакцію 150-200 мс. Ти на їхньому рівні!"),
    ((201, 250), "Це середня реакція людини на візуальний подразник. Чудовий результат!"),
    ((251, 300), "Такий час реакції дозволяє вчасно ухилитися від хука Франко!"),
    ((301, 350), "Приблизно стільки часу потрібно гепарду, щоб зробити один крок на повній швидкості."),
    ((351, 400), "Ти все ще швидший, ніж середня реакція на звуковий сигнал (~400 мс)."),
    ((401, 500), "Це час, за який колібрі робить близько 20 помахів крилами."),
    ((501, 700), "Непогано! Продовжуй тренуватися, і ти станеш ще швидшим!"),
    ((701, 1000), "Можливо, ти трохи відволікся? Спробуй ще раз, ти можеш краще!"),
    ((1001, 9999), "Схоже, ти вирішив перевірити, чи я не заснув. Я не заснув! :)"),
]

def get_fact_for_time(time_ms: int) -> str:
    """
    Повертає відповідний факт для заданого часу реакції.
    """
    for (min_ms, max_ms), fact in REACTION_FACTS:
        if min_ms <= time_ms <= max_ms:
            return fact
    return "Цікаво, але для такого часу в мене ще немає факту!"
