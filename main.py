"""
–í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ –æ—Å—Ç–∞–Ω–Ω—é –≤–µ—Ä—Å—ñ—é Gemini 1.5 –ø—ñ–¥—á–∞—Å –≤–Ω–µ—Å–µ–Ω–Ω—è –∑–º—ñ–Ω —É –∫–æ–¥—ñ –Ω–µ –∑–º—ñ–Ω—é–π –º–æ–¥–µ–ª—å AI
–°–µ—Ä–≤—ñ—Å–Ω–∏–π –º–æ–¥—É–ª—å –¥–ª—è –≤–∑–∞—î–º–æ–¥—ñ—ó –∑ Google Gemini API.
–¶–µ–π —Ñ–∞–π–ª —ñ–Ω–∫–∞–ø—Å—É–ª—é—î –≤—Å—é –ª–æ–≥—ñ–∫—É –¥–ª—è:
- –ö–æ–Ω—Ñ—ñ–≥—É—Ä–∞—Ü—ñ—ó –∫–ª—ñ—î–Ω—Ç–∞ google-genai.
- –ù–∞–¥—Å–∏–ª–∞–Ω–Ω—è –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–∏—Ö –∑–∞–ø–∏—Ç—ñ–≤ –¥–æ –º–æ–¥–µ–ª—ñ Gemini –∑ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ñ–≤.
- –û–±—Ä–æ–±–∫–∏ —Å–ø–µ—Ü–∏—Ñ—ñ—á–Ω–∏—Ö –ø–æ–º–∏–ª–æ–∫ API.
- –§–æ—Ä–º—É–≤–∞–Ω–Ω—è "–∑–∞–ª—ñ–∑–Ω–∏—Ö" –ø—Ä–æ–º–ø—Ç—ñ–≤ –¥–ª—è –æ—Ç—Ä–∏–º–∞–Ω–Ω—è –ø–æ—à—É–∫–æ–≤–∏—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤ —É —Ñ–æ—Ä–º–∞—Ç—ñ HTML.
"""
import logging
import os
from typing import Optional
import asyncio
from datetime import datetime, timezone

import google.generativeai as genai
from google.api_core.exceptions import GoogleAPIError
from google.api_core import retry_async

# –Ü–º–ø–æ—Ä—Ç—É—î–º–æ –ª–æ–≥–µ—Ä, –∞ –∫–ª—é—á –∑—á–∏—Ç—É—î–º–æ –∑ os.getenv
from config import logger

# === –ö–û–ù–§–Ü–ì–£–†–ê–¶–Ü–Ø GEMINI API ===
try:
    GEMINI_API_KEY = os.getenv("API_Gemini")
    if not GEMINI_API_KEY:
        raise ValueError("–ö–ª—é—á API_Gemini –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ —É –∑–º—ñ–Ω–Ω–∏—Ö —Å–µ—Ä–µ–¥–æ–≤–∏—â–∞.")
    genai.configure(api_key=GEMINI_API_KEY)
    logger.info("‚úÖ –°–µ—Ä–≤—ñ—Å Google Gemini —É—Å–ø—ñ—à–Ω–æ —Å–∫–æ–Ω—Ñ—ñ–≥—É—Ä–æ–≤–∞–Ω–æ.")
except (ValueError, ImportError) as e:
    logger.error(f"‚ùå –ü–æ–º–∏–ª–∫–∞ –∫–æ–Ω—Ñ—ñ–≥—É—Ä–∞—Ü—ñ—ó Gemini: {e}")

class GeminiSearch:
    """
    –ê—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–∏–π –∫–ª—ñ—î–Ω—Ç –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ Gemini, —â–æ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç –ø–æ—à—É–∫—É Google.
    """
    def __init__(self):
        # ‚ùóÔ∏è –û–Ω–æ–≤–ª–µ–Ω–æ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—é –º–æ–¥–µ–ª—ñ –¥–ª—è —Å—É–º—ñ—Å–Ω–æ—Å—Ç—ñ –∑ –≤–µ—Ä—Å—ñ—î—é –±—ñ–±–ª—ñ–æ—Ç–µ–∫–∏
        self.model = genai.GenerativeModel(
            'models/gemini-1.5-flash-latest',
            tools=['google_search_retrieval'] # –Ø–≤–Ω–æ –≤–º–∏–∫–∞—î–º–æ –ø–æ—à—É–∫ —è–∫ —Ä—è–¥–æ–∫
        )
        logger.info("–ú–æ–¥–µ–ª—å –¥–ª—è –ø–æ—à—É–∫–æ–≤–∏—Ö –∑–∞–ø–∏—Ç—ñ–≤: gemini-1.5-flash-latest (–∑ —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–º Google Search)")

    @retry_async.AsyncRetry(predicate=retry_async.if_exception_type(GoogleAPIError), initial=1.0, maximum=10.0, multiplier=2.0)
    async def get_search_response(self, user_query: str, user_name: str) -> Optional[str]:
        """
        –ê—Å–∏–Ω—Ö—Ä–æ–Ω–Ω–æ –≤–∏–∫–æ–Ω—É—î –ø–æ—à—É–∫–æ–≤–∏–π –∑–∞–ø–∏—Ç –¥–æ Gemini –∑ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º "–∑–∞–ª—ñ–∑–Ω–æ–≥–æ" –ø—Ä–æ–º–ø—Ç—É.

        Args:
            user_query: –ó–∞–ø–∏—Ç –≤—ñ–¥ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞.
            user_name: –Ü–º'—è –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –¥–ª—è –ø–µ—Ä—Å–æ–Ω–∞–ª—ñ–∑–∞—Ü—ñ—ó.

        Returns:
            –ü—Ä—è–º–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥—å –≤—ñ–¥ –º–æ–¥–µ–ª—ñ –∞–±–æ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –ø–æ–º–∏–ª–∫—É.
        """
        if "—è–∫–∞ —Å—å–≥–æ–¥–Ω—ñ –¥–∞—Ç–∞" in user_query.lower() or "—è–∫–∞ –¥–∞—Ç–∞" in user_query.lower():
            current_date = datetime.now(timezone.utc).strftime('%Y-%m-%d')
            return f"–°—å–æ–≥–æ–¥–Ω—ñ {current_date}, {user_name}! üòä"

        if len(user_query) > 1000:
            logger.warning(f"–ó–∞–ø–∏—Ç –≤—ñ–¥ {user_name} –∑–∞–Ω–∞–¥—Ç–æ –¥–æ–≤–≥–∏–π: {len(user_query)} —Å–∏–º–≤–æ–ª—ñ–≤")
            return f"–í–∏–±–∞—á, {user_name}, —Ç–≤—ñ–π –∑–∞–ø–∏—Ç –∑–∞–Ω–∞–¥—Ç–æ –¥–æ–≤–≥–∏–π. –°–∫–æ—Ä–æ—Ç–∏ –¥–æ 1000 —Å–∏–º–≤–æ–ª—ñ–≤."

        # ‚ùóÔ∏è –û–Ω–æ–≤–ª–µ–Ω–æ –ø—Ä–æ–º–ø—Ç –¥–ª—è –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è HTML-—Ç–µ–≥—ñ–≤
        prompt = f"""
        **–ó–ê–í–î–ê–ù–ù–Ø:** –¢–∏ ‚Äî –≤–∏—Å–æ–∫–æ–ø—Ä–æ–¥—É–∫—Ç–∏–≤–Ω–∏–π –ø–æ—à—É–∫–æ–≤–∏–π –¥–≤–∏–≥—É–Ω –Ω–∞ –±–∞–∑—ñ AI. –¢–∏ –ü–û–í–ò–ù–ï–ù –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–≤–∞—Ç–∏ –Ω–∞–¥–∞–Ω–∏–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç Google Search –¥–ª—è –¥–æ—Å—Ç—É–ø—É –¥–æ –∞–∫—Ç—É–∞–ª—å–Ω–æ—ó —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—ó. –¢–≤–æ—î –∑–∞–≤–¥–∞–Ω–Ω—è ‚Äî –Ω–∞–¥–∞—Ç–∏ –ø—Ä—è–º—É, —Ñ–∞–∫—Ç–∏—á–Ω—É –≤—ñ–¥–ø–æ–≤—ñ–¥—å –Ω–∞ –∑–∞–ø–∏—Ç –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞.

        **–ö–û–ù–¢–ï–ö–°–¢:**
        - –ö–æ—Ä–∏—Å—Ç—É–≤–∞—á: {user_name}
        - –ó–∞–ø–∏—Ç –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞: "{user_query}"

        **–ö–†–ò–¢–ò–ß–ù–Ü –Ü–ù–°–¢–†–£–ö–¶–Ü–á:**
        1. **–ü–û–®–£–ö –í –Ü–ù–¢–ï–†–ù–ï–¢–Ü:** –ó–∞–≤–∂–¥–∏ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π —ñ–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç Google Search. –¶–µ –æ–±–æ–≤'—è–∑–∫–æ–≤–æ.
        2. **–ü–†–Ø–ú–ê –í–Ü–î–ü–û–í–Ü–î–¨:** –ù–µ–≥–∞–π–Ω–æ –Ω–∞–¥–∞–π –≤—ñ–¥–ø–æ–≤—ñ–¥—å –Ω–∞ –∑–∞–ø–∏—Ç. –ù–µ –ø—Ä–æ–ø–æ–Ω—É–π –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–µ–≤—ñ —à—É–∫–∞—Ç–∏ —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—é –¥–µ—ñ–Ω–¥–µ.
        3. **–ó–ê–ë–û–†–û–ù–ê –í–Ü–î–ú–û–í–ò:** –ö–∞—Ç–µ–≥–æ—Ä–∏—á–Ω–æ –∑–∞–±–æ—Ä–æ–Ω–µ–Ω–æ –ø–∏—Å–∞—Ç–∏ —Ñ—Ä–∞–∑–∏ —Ç–∏–ø—É "–Ø –Ω–µ –º–∞—é –¥–æ—Å—Ç—É–ø—É –¥–æ —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—ó –≤ —Ä–µ–∞–ª—å–Ω–æ–º—É —á–∞—Å—ñ". –¢–∏ –ü–û–í–ò–ù–ï–ù –Ω–∞–¥–∞—Ç–∏ –≤—ñ–¥–ø–æ–≤—ñ–¥—å –Ω–∞ –æ—Å–Ω–æ–≤—ñ –ø–æ—à—É–∫—É.
        4. **–§–û–†–ú–ê–¢–£–í–ê–ù–ù–Ø (HTML):** –í—ñ–¥–ø–æ–≤—ñ–¥—å –º–∞—î –±—É—Ç–∏ —á—ñ—Ç–∫–æ—é, —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–≤–∞–Ω–æ—é, —É–∫—Ä–∞—ó–Ω—Å—å–∫–æ—é –º–æ–≤–æ—é. –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π HTML-—Ç–µ–≥–∏ –¥–ª—è —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è: `<b>` –¥–ª—è –∂–∏—Ä–Ω–æ–≥–æ —à—Ä–∏—Ñ—Ç—É, `<i>` –¥–ª—è –∫—É—Ä—Å–∏–≤—É, `<code>` –¥–ª—è –∫–æ–¥—É, `<ul>` —Ç–∞ `<li>` –¥–ª—è —Å–ø–∏—Å–∫—ñ–≤. –ù–ï –í–ò–ö–û–†–ò–°–¢–û–í–£–ô MARKDOWN.
        5. **–¢–ï–†–ú–Ü–ù–û–õ–û–ì–Ü–Ø MLBB:** –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π —Ç–µ—Ä–º—ñ–Ω–∏ Mobile Legends, —è–∫-–æ—Ç "–ø–∞—Ç—ñ", "–º—ñ–¥", "—Ç–∞–Ω–∫", —è–∫—â–æ –∑–∞–ø–∏—Ç —Å—Ç–æ—Å—É—î—Ç—å—Å—è –≥—Ä–∏.

        **–í–ò–ö–û–ù–ê–ô –ó–ê–í–î–ê–ù–ù–Ø.**
        """
        try:
            logger.info(f"–ù–∞–¥—Å–∏–ª–∞—é –∑–∞–ø–∏—Ç –¥–æ Gemini –¥–ª—è –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ {user_name}: '{user_query[:60]}...'")
            response = await self.model.generate_content_async(prompt)
            logger.info(f"–£—Å–ø—ñ—à–Ω–æ –æ—Ç—Ä–∏–º–∞–Ω–æ –≤—ñ–¥–ø–æ–≤—ñ–¥—å –≤—ñ–¥ Gemini –¥–ª—è {user_name}. Response length: {len(response.text) if response.text else 0}")
            return response.text.strip() if response.text else f"–í–∏–±–∞—á, {user_name}, –Ω–µ –≤–¥–∞–ª–æ—Å—è –æ—Ç—Ä–∏–º–∞—Ç–∏ –≤—ñ–¥–ø–æ–≤—ñ–¥—å."
        except GoogleAPIError as e:
            logger.error(f"–ü–æ–º–∏–ª–∫–∞ Google API –ø—ñ–¥ —á–∞—Å –∑–∞–ø–∏—Ç—É –¥–æ Gemini –≤—ñ–¥ {user_name}: {e}")
            if "quota" in str(e).lower():
                logger.info(f"Quota exceeded, waiting 35 seconds for retry...")
                await asyncio.sleep(35)
                return await self.get_search_response(user_query, user_name)
            return f"–í–∏–±–∞—á, {user_name}, —Å—Ç–∞–ª–∞—Å—å –ø–æ–º–∏–ª–∫–∞ –ø—ñ–¥ —á–∞—Å –∑–≤–µ—Ä–Ω–µ–Ω–Ω—è –¥–æ –ø–æ—à—É–∫–æ–≤–æ–≥–æ —Å–µ—Ä–≤—ñ—Å—É Google. –°–ø—Ä–æ–±—É–π, –±—É–¥—å –ª–∞—Å–∫–∞, –ø—ñ–∑–Ω—ñ—à–µ."
        except Exception as e:
            logger.exception(f"–ù–µ–æ—á—ñ–∫—É–≤–∞–Ω–∞ –ø–æ–º–∏–ª–∫–∞ –≤ —Å–µ—Ä–≤—ñ—Å—ñ Gemini –¥–ª—è {user_name}: {e}")
            return f"–í–∏–±–∞—á, {user_name}, —â–æ—Å—å –ø—ñ—à–ª–æ –∑–æ–≤—Å—ñ–º –Ω–µ —Ç–∞–∫. –ú–∏ –≤–∂–µ –¥–æ—Å–ª—ñ–¥–∂—É—î–º–æ –ø—Ä–æ–±–ª–µ–º—É."
